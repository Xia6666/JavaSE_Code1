package com.itheima.extends01.test03;

public class Person {

    private String id;
    private String name;
    private int age;
    private String birthday;

    public Person() {
    }

    public Person(String id, String name, int age, String birthday) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.birthday = birthday;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public void show()
    {
        System.out.println(id+""+name+""+age+""+birthday+"");
    }

}
